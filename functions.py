def clean_filename(name: str, max_length: int = 255) -> str:
    # –£–¥–∞–ª—è–µ–º –∑–∞–ø—Ä–µ—â—ë–Ω–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –¥–ª—è Windows
    name = re.sub(r'[<>:"/\\|?*]', '', name)
    # –ó–∞—Ä–µ–∑–µ—Ä–≤–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∏–º–µ–Ω–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, CON.xlsx)
    reserved = {'CON', 'PRN', 'AUX', 'NUL', *(f'COM{i}' for i in range(1, 10)), *(f'LPT{i}' for i in range(1, 10))}
    # –£–¥–∞–ª—è–µ–º –ø—Ä–æ–±–µ–ª—ã –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ
    name = name.strip()
    # –£–¥–∞–ª—è–µ–º —Ä–∞—Å—à–∏—Ä–µ–Ω–∏–µ –ø–µ—Ä–µ–¥ –ø—Ä–æ–≤–µ—Ä–∫–æ–π –∏–º–µ–Ω–∏
    base = name.rsplit('.', 1)[0]
    # –ü–µ—Ä–µ–∏–º–µ–Ω–æ–≤—ã–≤–∞–µ–º –∑–∞—Ä–µ–∑–µ—Ä–≤–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ
    if base.upper() in reserved:
        base = f"{base}_safe"
    # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ–º –¥–ª–∏–Ω—ã

    base = base.replace('–ñ–ö ', '')  # –£–±–∏—Ä–∞–µ–º '–ñ–ö '
    base = base.strip('¬´¬ª"')  # –£–±–∏—Ä–∞–µ–º –∫–∞–≤—ã—á–∫–∏ ¬´¬ª, ""

    return f"{base[:max_length - 5]}.xlsx"  # 5 —Å–∏–º–≤–æ–ª–æ–≤ –ø–æ–¥ ".xlsx"

